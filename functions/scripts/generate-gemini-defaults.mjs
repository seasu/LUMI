#!/usr/bin/env node
/**
 * Generates src/geminiDefaults.generated.ts from environment variables.
 * CI: GitHub Actions Repository Variables (same names as env vars below).
 *
 * Env vars (all optional — defaults shown):
 *   GEMINI_DEFAULT_VISION_MODEL       default: gemini-2.0-flash
 *   GEMINI_DEFAULT_EMBEDDING_MODEL    default: text-embedding-004
 *   GEMINI_DEPRECATED_VISION_MODELS   comma-separated
 *   GEMINI_DEPRECATED_EMBEDDING_MODELS comma-separated (optional)
 *   GEMINI_VISION_FALLBACK_CHAIN       comma-separated
 *   GEMINI_EMBEDDING_FALLBACK_CHAIN    comma-separated
 */
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, "..", "src", "geminiDefaults.generated.ts");

const defaultVision =
  process.env.GEMINI_DEFAULT_VISION_MODEL?.trim() || "gemini-2.0-flash";
const defaultEmbedding =
  process.env.GEMINI_DEFAULT_EMBEDDING_MODEL?.trim() || "text-embedding-004";

const deprecatedVisionRaw =
  process.env.GEMINI_DEPRECATED_VISION_MODELS?.trim() ||
  "gemini-1.5-flash,gemini-1.5-flash-8b,gemini-1.5-pro";

const deprecatedEmbeddingRaw =
  process.env.GEMINI_DEPRECATED_EMBEDDING_MODELS?.trim() || "";

const fallbackVisionRaw =
  process.env.GEMINI_VISION_FALLBACK_CHAIN?.trim() ||
  "gemini-2.0-flash,gemini-flash-latest,gemini-2.5-flash";

const fallbackEmbeddingRaw =
  process.env.GEMINI_EMBEDDING_FALLBACK_CHAIN?.trim() ||
  "text-embedding-004,text-embedding-001";

function splitCsv(s) {
  return s
    .split(",")
    .map((x) => x.trim())
    .filter(Boolean);
}

const deprecatedVision = splitCsv(deprecatedVisionRaw);
const deprecatedEmbedding = splitCsv(deprecatedEmbeddingRaw);
const visionFallbackChain = splitCsv(fallbackVisionRaw);
const embeddingFallbackChain = splitCsv(fallbackEmbeddingRaw);

const esc = (s) => s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');

const content = `// AUTO-GENERATED by scripts/generate-gemini-defaults.mjs — do not edit.
// Regenerate: node scripts/generate-gemini-defaults.mjs
// CI: GitHub Variables → same env names, then npm run build.

export const geminiGeneratedDefaults = {
  defaultVisionModel: "${esc(defaultVision)}",
  defaultEmbeddingModel: "${esc(defaultEmbedding)}",
  deprecatedVisionModelIds: ${JSON.stringify(deprecatedVision)},
  deprecatedEmbeddingModelIds: ${JSON.stringify(deprecatedEmbedding)},
  visionFallbackChain: ${JSON.stringify(visionFallbackChain)},
  embeddingFallbackChain: ${JSON.stringify(embeddingFallbackChain)},
} as const;
`;

writeFileSync(outPath, content, "utf8");
console.log(`Wrote ${outPath}`);
